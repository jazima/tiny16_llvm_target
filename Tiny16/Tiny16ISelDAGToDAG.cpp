void Tiny16DAGToDAGISel::SelectMoveImmediate(SDNode *N) {
  std::cout << "[TINY16] Tiny16DAGToDAGISel::SelectMoveImmediate called\n";
  // Make sure the immediate size is supported.
  ConstantSDNode *ConstVal = cast<ConstantSDNode>(N);
  uint64_t ImmVal = ConstVal->getZExtValue();
  uint64_t SupportedMask = 0xfffff;
  if ((ImmVal & SupportedMask) != ImmVal) {
    return SelectCode(N);
  }

  // Special case for 0 - use R0
  if (ImmVal == 0) {
    ReplaceNode(N, CurDAG->getRegister(Tiny16::R0, N->getValueType(0))
                       .getNode()); // Special case: 0 - use R0
    return;
  }

  // If value is less than 256, use only movlo
  // Select the low part of the immediate move.
  uint64_t LoMask = 0xff;
  uint64_t HiMask = 0xff00;
  uint64_t ImmLo = (ImmVal & LoMask);
  uint64_t ImmHi = (ImmVal & HiMask) >> 8;
  SDValue ConstLo = CurDAG->getTargetConstant(ImmLo, N, MVT::i16);
  MachineSDNode *Move =
      CurDAG->getMachineNode(Tiny16::MOVI, N, MVT::i16, ConstLo);

  if (ImmVal > 256) {
    SDValue ConstHi = CurDAG->getTargetConstant(ImmHi, N, MVT::i16);
    Move = CurDAG->getMachineNode(Tiny16::MOVHI, N, MVT::i16, SDValue(Move, 0),
                                  ConstHi);
  }

  ReplaceNode(N, Move);
}

void Tiny16DAGToDAGISel::Select(SDNode *N) {
  std::cout << "[TINY16] Tiny16DAGToDAGISel::Select called\n";
  switch (N->getOpcode()) {
  case ISD::Constant:
    SelectMoveImmediate(N);
    return;
    // ISD::BR_CC is handled by Lowering -> Tiny16ISD::BR_CC -> SelectCode
    // matches pattern
  }

  SelectCode(N);
}

// createTiny16ISelDag - This pass converts a legalized DAG into a
// Tiny16-specific DAG, ready for instruction scheduling.

FunctionPass *llvm::createTiny16ISelDag(Tiny16TargetMachine &TM) {
  std::cout << "[TINY16] createTiny16ISelDag called\n";
  return new Tiny16DAGToDAGISelLegacy(TM);
}
